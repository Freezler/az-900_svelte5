	<h2 class="text-3xl text-center text-sky-400">Cloud Computing</h2>
	<p class="">
		Service delivery model over the internet (cloud). This includes, but is not limited to, compute
		power, meaning servers such as Windows, Linux, hosting environments, etc.; storage like files
		and/or databases; networking in Azure but also outside when connecting to your company network;
		and analytics services for visualization and telemetry data.
	</p>
