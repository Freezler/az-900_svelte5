		<h2 class="text-3xl pb-4 mx-auto text-sky-400 mt-10  ">Key concepts</h2>
		<ul class="list-inside list-disc text-left mx-2 md:w-[40ch] md:mx-auto  ">
			<li>
				<strong>Scalability:</strong> The ability to scale, so allocate and deallocate resources at any
				time.
			</li>
			<li><strong>Elasticity:</strong> The ability to scale dynamically.</li>
			<li><strong>Agility:</strong> The ability to react fast (scale quickly).</li>
			<li>
				<strong>Fault Tolerance:</strong> The ability to maintain system uptime while physical and service
				component failures happen.
			</li>
			<li>
				<strong>Disaster Recovery:</strong> The process and design principle which allows a system to
				recover from natural or human-induced disasters.
			</li>
			<li>
				<strong>High Availability:</strong> The agreed level of operational uptime for the system.<br /> It
				is a simple calculation of system uptime versus the whole lifetime of the system:<br /> <pre class="uppercase underline underline-offset-8 text-semibold text-center text-sky-400  pt-4 text-[clamp(0.8rem,2vw,1rem)]">availability = uptime / (uptime + downtime)</pre>
			</li>
		</ul>
