			<h2 class="text-3xl text-sky-400 mb-6 mt-4">Key concepts</h2>
		</header>
		<ul
			class="mb-4 w-[28ch] list-inside list-disc text-left sm:w-[45ch] md:mx-auto md:w-[35ch] lg:w-[50ch] xl:w-[60ch] grid gri"
